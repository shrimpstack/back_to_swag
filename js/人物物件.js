function 新增物件_人物({重力倍數} = {}) {
  let 物件 = 新增物件("人物");
  if(!物件) return;

  /* 重力 */
  物件屬性_重力(物件);
  if(typeof 重力倍數 == "number") 物件.重力倍數 = 重力倍數;

  /* 碰撞 */
  新增碰撞箱("主要", 物件)
  .設定碰撞圖層("人物")
  .設定大小(36, 72)
  .設定推擠等級("人物");
  物件.啟用碰撞箱("主要");

  /* 顯示 */
  物件.style.width = "36px";
  物件.style.height = "72px";
  物件.style.background = "#faa";

  return 物件;
}